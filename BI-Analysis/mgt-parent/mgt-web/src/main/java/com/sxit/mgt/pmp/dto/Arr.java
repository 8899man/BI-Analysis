package com.sxit.mgt.pmp.dto;

public class Arr {
    private String name;
    
	private Double percent;
	
	public Arr(){
		super();
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getPercent() {
		return percent;
	}
	public void setPercent(Double percent) {
		this.percent = percent;
	}
}
